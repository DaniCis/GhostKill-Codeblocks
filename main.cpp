static bool Colision()
{
    double rectx;
    double recty;
    while(pos_x>Datos_Columnas[fila][0]+Ancho_Columna)
    {
        fila++;
        score++;
        PlaySound("\coin.wav",NULL,SND_ASYNC|SND_FILENAME);
    }
    rectx=Datos_Columnas[fila][0];
    recty=Datos_Columnas[fila][1];

    if(pos_x>=rectx-5)
    {
        if(pos_y<recty || pos_y>recty+20)
        {
            return true;
        }
    }
    return false;
}

void Teclado(unsigned char key, int, int)
{
    switch(key)
    {
        case 32:
            if(flag!=false && Colision()==false)
            {
                velocidad_x=0.1;
                aceyconst=0.003;
                acey=0.3;
                PlaySound("\jump.wav",NULL,SND_ASYNC);
            }
            break;
        case 13:
            if(flag==false)
            {
                    flag=true;
            }
            else if(pasarmenu==false)
            {
                pasarmenu=true;
                flag=false;
            }
            break;

    }
    glutPostRedisplay();
}
void IluminacionMaterial(GLfloat ambientR, GLfloat ambientG, GLfloat ambientB, GLfloat diffuseR, GLfloat diffuseG, GLfloat diffuseB, GLfloat specularR, GLfloat specularG, GLfloat specularB, GLfloat shininess)
{
                     GLfloat ambient[]= {ambientR, ambientG, ambientB};
                     GLfloat diffuse[]= {diffuseR, diffuseG, diffuseB};
                     GLfloat specular[]= {specularR, specularG, specularB};
                     glMaterialfv(GL_FRONT,GL_AMBIENT, ambient);
                     glMaterialfv(GL_FRONT,GL_DIFFUSE, diffuse);
                     glMaterialfv(GL_FRONT,GL_SPECULAR, specular);
                     glMaterialf(GL_FRONT, GL_SHININESS, shininess);//un solo valor flotante y no vector
}
void Iluminacion()
{
    GLfloat light_ambient[] = {1.0,1.0,1.0,1.0};
        GLfloat light_specular[]= {1.0,0.5,0.5,1.0};
        GLfloat light_diffuse[]= {1.0,1.0,1.0,0.0};
        GLfloat light_position[]= {50.0,50.0,50.0,1.0};
        glEnable(GL_LIGHTING);
        glLightModelfv(GL_LIGHT_MODEL_AMBIENT, light_ambient);//se usa el light_model_ambient como parámetro
        glEnable(GL_LIGHT5);
        glLightfv(GL_LIGHT5,GL_POSITION, light_position);
        glLightfv(GL_LIGHT5,GL_AMBIENT,light_ambient);
        glLightfv(GL_LIGHT5,GL_DIFFUSE,light_diffuse);
        glLightfv(GL_LIGHT5,GL_SPECULAR, light_specular);
}
